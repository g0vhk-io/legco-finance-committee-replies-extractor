package com.g0vhk.finance.extractor;

public class InitialReplyDirector {
	final String title;
	final String personel;
	
	public InitialReplyDirector(String title, String personel) {
		super();
		this.title = title;
		this.personel = personel;
	}

	public String getTitle() {
		return title;
	}

	public String getPersonel() {
		return personel;
	}
	
	
}
