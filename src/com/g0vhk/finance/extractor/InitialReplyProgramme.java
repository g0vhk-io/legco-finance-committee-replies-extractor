package com.g0vhk.finance.extractor;

public class InitialReplyProgramme {
	final String title;
	final int index;
	
	public InitialReplyProgramme(String title, int index) {
		super();
		this.title = title;
		this.index = index;
	}

	public String getTitle() {
		return title;
	}

	public int getIndex() {
		return index;
	}
	
	
}
