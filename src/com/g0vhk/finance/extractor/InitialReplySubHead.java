package com.g0vhk.finance.extractor;

public class InitialReplySubHead {
	final int index;
	final String title;
	
	public InitialReplySubHead(int index, String title) {
		super();
		this.index = index;
		this.title = title;
	}

	public int getIndex() {
		return index;
	}

	public String getTitle() {
		return title;
	}
}
